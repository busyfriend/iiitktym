<h2>Welcome to IIIT Kottayam</h2><hr>
<p>The Ministry of Human Resources Development, Government of India has put forward a scheme wherein 20 new IIITs are proposed to be established in the country. A major objective in establishing IIITs is to set up an education model which can produce best-in-class human resources in IT and which can harness the multidimensional facets of IT in various domains. The IIIT is expected to contribute significantly to the global competitiveness through the key sectors of Indian economy and Industry, focusing on applied research and education in IT in selected domain areas.</p>
<p>Accordingly, setting up of a IIIT in Kottayam district under the 20-IIIT scheme of Government of India is sanctioned. The initial investment involved is 128 Crores, 64 Crores being grant from the Central Government, 45 crores grant from Government of Kerala and 19 crores raised through private participation. In addition, the state will provide necessary land free of cost. The IIIT shall be located at Pala in Kottayam District. The land acquired is located at Vallichira village, Block No.20, Kottayam District.</p>
